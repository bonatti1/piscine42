#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int aux;
	int i;
	int j;

	i = 0;
	j = 1;
	while (i < size)
	{
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				aux = tab[i];
				tab[i] = tab[j];
				tab[j] = aux;
			}
			j++;
		}
		i++;
		j = i + 1;
	}
}
